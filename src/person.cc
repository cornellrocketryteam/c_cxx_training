#include <iostream>
#include "person.h"

//-----------------------------------------------------
// Constructor
//-----------------------------------------------------

Person::Person( std::string init_name, int init_age )
{
    name = init_name;
    age  = init_age;
}

//-----------------------------------------------------
// get_name
//-----------------------------------------------------

std::string Person::get_name()
{
    return name;
}

//-----------------------------------------------------
// get_age
//-----------------------------------------------------

int Person::get_age()
{
    return age;
}

//-----------------------------------------------------
// set_name
//-----------------------------------------------------

void Person::set_name( std::string new_name )
{
    name = new_name;
}

//-----------------------------------------------------
// set_age
//-----------------------------------------------------

void Person::set_age ( int new_age )
{
    age = new_age;
}

//-----------------------------------------------------
// print_person
//-----------------------------------------------------

void Person::print_person()
{
    std::cout << "Name: " << name << std::endl;
    std::cout << " - Age: " << age << std::endl;
}